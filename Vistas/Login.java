    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        //1.
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        //2.
        System.out.println("username: " + username + ",password: " + password);
        //3.
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Hay Campos sin diligenciar", "Informacion", JOptionPane.WARNING_MESSAGE);
        } else {
            if (username.equals("MisionTIC") && password.equals("Ciclo2")) {
                JOptionPane.showMessageDialog(null, "Bienvenido al sistema MISION TIC");
                UserMenu usermenu = new UserMenu();
                usermenu.setVisible(rootPaneCheckingEnabled);

                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Error al iniciar sesi칩n", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnloginActionPerformed

